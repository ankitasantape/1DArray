package Basic;

public class FindRange {

	public static void main(String[] args) {
		byte b = 126;
		System.out.println(b);
		b++;
		System.out.println(b);
        ++b;
        System.out.println(b);
        ++b;
        System.out.println(b);
        short a = 32767;
        System.out.println(a);
        ++a;
        System.out.println(a);
        int i = 2147483647;
        System.out.println(i);
        i++;
        System.out.println(i);
        char c = 255;
        System.out.println(c);
        c++;
        System.out.println(c);
        c++;
        System.out.println(c);
	}

}
